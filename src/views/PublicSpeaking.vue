      </ul>

      <h2 id="empathic-imitation">Empathic Imitation</h2>
      <ul>
        <li>
          Children grasp concepts holistically through imitation, not to be confused with copying by wrote
        </li>
        <li>
          Rather, driven by a feeling of attraction resulting from a mysterious process of a <strong>will to
          apprehend “a whole” by inhabiting the subjects’ body and experience.</strong> Imagine the circle time of
          the children’s garden with the movement, songs and stories – which abstracts further as the
          child ages into sitting at a desk and learning topics more “traditionally” and reflecting
          through creative (handwork, woodwork, art) projects.
        </li>
        <li>
          <strong><i>This is Empathic Imitation</i></strong>
        </li>
        <li>
          Ultimately, through this, values can be developed which collectively form identity
        </li>
        <li>
          This leads us to the importance of attention itself…
        </li>
      </ul>

      <h2 id="attention-moral">Attention is a moral act: A reciprocal process with consequences</h2>
      <ul>
        <li>
          We pay attention differently to a man dying before our eyes than to a sunset. To a doctor,
          the dying man might be a case study of a pathology – but if this man is a loved one the attention
          is of a different nature. The sunset may be an everyday occurrence or a deep non-dualistic experience.
        </li>
        <li>
          Here we learn that the attention we pay to a thing determines what we find there
        </li>
        <li>
          <strong>What a thing is depends on who is attending to it and in what way</strong>
        </li>
        <li>
          <strong><i>Attention has consequences</i></strong>
        </li>
        <li>
          Another way to express this: We neither invent a subjective reality nor do we discover an
          objective or intersubjective reality rather there is a process of responsive evocation
        </li>
      </ul>

      <h2 id="responsive-evocation">Responsive Evocation: The Culminating Concept</h2>
      <ul>
        <li>
          <strong><i>The world calls forth something in me that in turn calls forth something in the world.</i></strong>
          This is the essence of creativity and the essence of learning in this community
        </li>
        <li>
          This is true of perceptive qualities which extend into academics
        </li>
        <li>
          The stoniness of the mountain is evoked through the chemistry of the subject and the one perceiving
          coming together and this <strong>qualitative experience equally depends on both parties</strong>
        </li>
      </ul>

