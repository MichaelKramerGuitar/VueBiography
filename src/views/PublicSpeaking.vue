      <h2 id="head-hand-heart">Head, Hand, Heart</h2>
      <ul>
        <li>
          What makes this community special is the attendance to the Head, Hand and Heart throughout
          each developmental stage
        </li>
        <li>This philosophy balances the educational approach from the nursery years through the
          high school years
        </li>
        <li>
          There is a <strong>symbiotic relationship</strong>
          where the <strong>lead teachers</strong> help both <strong>children and parents </strong> find a
          healthy balance
        </li>
        <li>
          The emphasis here is on the <strong>development of capacities for learning grounded in bodily experience
          and soaked in empathy</strong>
        </li>
        <li>
          Developing learning capacity is to learn <strong>how to think, not what to think.</strong> The child’s
          head, hand and heart are shaped together as a creative empty vessel, rather than assuming the
          child’s mind is already an empty vessel waiting to be filled with “useful” information
        </li>
        <li>
          I consider how different the world was when I started grade school in the late 90s from the
          time I graduated from high school in 2007 from now and am grateful for the emphasis on capacity
          development over a benchmark approach to education – and further wish this for my children
        </li>
        <li>
          <strong><i>The endgame is to be able to parse, interpret and internalize interdisciplinary abstract
            concepts including concepts that do not currently exist to make informed decisions in
            the world as an adult</i></strong>
        </li>
      </ul>

      <h2 id="conceptual-metaphor">Conceptual Metaphor makes abstract though possible</h2>
      <ul>
        <li>
          Implemented at Waldorf through Fables, Mythology, stories and folded into early childhood
          education loading the slingshot into young adulthood academic rigor
        </li>
        <li>
          <strong>Any one thing can only be understood in terms of another thing</strong>
        </li>
        <li>
          Ultimately that understanding must come down to something that is experienced in the body,
          the necessary context for all human experience – <i>keep in mind the Head, Hand, Heart</i>
        </li>
        <li>
          Let’s consider <strong>language as the parent abstraction</strong> and the
          <strong>academic disciplines being implementations of specific communicative approaches</strong>,
          lenses or angles for examining the world.
          For example, studying History is a way to learn about the world such that we can communicate
          about the world through that lens
        </li>
        <li>
          At any developmental stage, language in this way is not a procedure, algorithm or set of
          rules/facts rather an <strong>EMBODIED</strong> skill all with origins lying in the
          <strong>EMPATHIC COMMUNICATION</strong>of music where they are deeply connected with the body
        </li>
      </ul>

