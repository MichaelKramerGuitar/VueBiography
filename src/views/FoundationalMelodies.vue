      <h2 id="melody-harmony">Melody and Harmony</h2>
      <p class="rotation">It’s my personal philosophy that <strong>melody and harmony</strong> are of the same fabric.
        <strong>Like space and time.</strong></p>
      <p> I love the analogy of space and time because in physics these are described as a fabric,
        a continuum. As humans, we experience these separately but that is our limited ability to
        experience the vast range of scale encompassed in the phrase &quot;to be&quot;. The addition of a
        massive body at a point in space, something like a planet or a star, bends surrounding space.
        There are two consequences of this: firstly, objects traveling in space follow the curvature of
        that space - this is one way to describe the force of gravity, the apple fell on Newtons head
        because it broke free from its branch and moved along the curvature of space until it was
        interrupted by Newtons head. Secondly, the apple’s experience of time changes relative to the
        extent of which the gravitational force is being exerted upon it. The closer it is to the earth
        the stronger the earth’s gravitational field is acting on the apple, the slower the apple
        experiences time <strong>RELATIVE</strong> to the apples still on the tree, further away from the mass exerting
        the force, the earth.</p>
