      <h2 id="imagery">Imagery</h2>

      <p class="rotation"><em>What does this have to do with Charlie Parker?</em></p>
      <ul>
        <li>
          The notes comprising the harmony of the moment are the massive objects scattered throughout space,
          bending its shape, like the planets and star of a solar system.
        </li>
        <li>
          The notes of a melodic line are comets soaring through a planetary system coming in and put of the
          gravitational pull of planets in their trajectory.
        </li>
        <li>
          Rhythmic feel is embodied by the aggregate of these individual relationships.
        </li>
        <li>
          It’s an imperfect analogy, yet it remains one of the most inspiring images I can relax into to get
          into a <b>flow state</b> while improvising. After all, the study of these melodies in learning to generate
          a comfortable and personal rhythmic feel is to the end of developing one’s voice as an improviser.
        </li>
      </ul>

      <h2 id="flow">Develop your own imagery - Fall into flow</h2>
        <em>Certainly different people conceptualize, visualize, imagine, and hear in their minds' ear
          in different ways - consequently, this thought might not be helpful to some, yet I’d still
          like to offer:</em>
      <ul>
        <li>
          Cultivate your own imagery for the intersection of harmony, melody and rhythm. The above
          remains the most powerful for me (the gravity image), however its root in my imagination
          probably stems from a lesson I had in college with my teacher
          <a href="https://music.umd.edu/directory/gerard-kunkel" target="_blank">Gerry Kunkel</a>. First,
          he had me go to a white board and make an “interesting” visual composition with only triangles.
          There were no rules, they could intersect, have any angle structure (acute, obtuse, right).
          I spent maybe five minutes on my drawing and when I was done he asked me to play the drawing.
        </li>
        <li>
          I don’t remember the tune we played, and can’t recall if I had been struggling with it
          previously, but I strongly recall the feeling of liberation in &quot;playing the drawing&quot;.
          It was not a literal thing, it was the imagery of a geometric world in 2 dimensional space,
          these intersecting triangles that opened up all sorts of musical gestures and thoughts
          spontaneously that I was discovering in my mind in the split second before playing them.
          And the heaviest thing about it was that the part of
          <a href="https://youtu.be/H_HEyzkycro" target="_blank">“making the changes”</a> seemed to be
          taking care of itself.
        </li>
        <li>
          I recall before this experience being rather frustrated with the process of transcribing and
          assimilating the fruits of those transcriptions as I had not yet learned to go beyond taking
          the transcribed line literally based on its original context. I was still in a place of
          &quot;plugging &quot; lines into other harmonic contexts that happened to fit. This process for me was
          the antithesis of flow. On one hand, I was in love with doing the work yet on the other hand
          I knew there was an ingredient missing in the process for creating my own melodies
          spontaneously beyond the analysis and assimilation of syntax through transcription.
        </li>
        <li>
          The triangle imagery experience was my introduction to that missing ingredient and sparked
          the beginning of what has helped me achieve my flow state in the context of an improvised performance.
        </li>
      </ul>
